package loops;

public class Forloop {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {

			System.out.println("Hello");
		}

		for (int i = 5; i <= 50; i = i + 5) {

			System.out.println(i);
		}

		for (int i = 1; i <= 10; i++) {

			System.out.println(5 * i);
		}

	}

}
